import re

# 定义要搜索的cid列表，这里直接复制用户给出的列表，并去重
cids = [
    "CN00025261", "CN00606037", "CN29962802", "CN36066071", "CN00458126",
    "CN36899464", "CN35805798", "CN37120305", "CN15540338", "CN35102051",
    "CN00619404", "CN37627870", "CN36968128", "CN37568021", "CN36999238",
    "CN37077944", "CN37352330", "CN19326902", "CN37201386", "CN37128516",
    "CN00874534", "CN18798253", "CN37533841", "CN35828257", "CN37374682",
    "CN37512170", "CN37082812", "CN11556283", "CN00470261", "CN00018323",
    "CN33434557", "CN37639305", "CN00677719", "CN37677458", "CN37265165",
    "CN10429675", "CN00252486", "CN00227475", "CN37585577", "CN37265165",
    "CN37471567", "CN37355908", "CN37570644", "CN37630928", "CN37464916",
    "CN00471674", "CN37186361", "CN00004527", "CN36610696", "CN37630928",
    "CN00233488", "CN36909145"
]

# 去重
unique_cids = set(cids)

# 读取文件
file_path = "BIOS_DATA/Concepts 20240125.txt"
with open(file_path, 'r', encoding='utf-8') as f:
    lines = f.readlines()

# 编译正则表达式，匹配任意一个cid，注意cid可能是独立的单词，所以我们用单词边界\b
# 但是注意，cid是数字和字母组成，且以CN开头，后面是数字，所以我们可以用以下正则表达式
# 但是，如果文件中cid可能与其他字符连在一起，我们可能需要更精确的匹配。假设cid是独立的，用\b包围
pattern = re.compile(r'\b(' + '|'.join(unique_cids) + r')\b')

# 存储匹配结果
matches = []
for line in lines:
    if pattern.search(line):
        matches.append(line.strip())

# 输出所有匹配的行
for match in matches:
    print(match)